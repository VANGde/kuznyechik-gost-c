#include "kuznyechik.h"

#include <stdio.h>

int main(void)
{
    block128 key128[2] = {{0x88, 0x99, 0xAA, 0xBB, 0xCC, 0xDD, 0xEE, 0xFF, 0x00, 0x11, 0x22, 0x33,
                           0x44, 0x55, 0x66, 0x77},
                          {0xFE, 0xDC, 0xBA, 0x98, 0x76, 0x54, 0x32, 0x10, 0x01, 0x23, 0x45, 0x67,
                           0x89, 0xAB, 0xCD, 0xEF}};
    block128 round_keys[10];
    gen_round_keys(key128, round_keys);

    block128 data = {0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x00,
                     0xFF, 0xEE, 0xDD, 0xCC, 0xBB, 0xAA, 0x99, 0x88};
    block128 encrypted, decrypted;
    kuznechik_encrypt(round_keys, &data, &encrypted);
    kuznechik_decrypt(round_keys, &encrypted, &decrypted);

    printf("Kuznyechik:\n");
    printf("Plain:  ");
    print_chunk(&data);
    printf("Cipher: ");
    print_chunk(&encrypted);
    printf("Decrypt:");
    print_chunk(&decrypted);
}
